// 단지 분양가 매핑 — supabase/058 의 get_apt_listing_price() 와 동일.
// 클라이언트에서 표시용으로 사용.
export function getAptListingPrice(lawdCd: string | null | undefined): number {
  if (!lawdCd) return 50;
  const map: Record<string, number> = {
    '11650': 240, // 서초구
    '11680': 233, // 강남구
    '11710': 181, // 송파구
    '11170': 180, // 용산구
    '11200': 151, // 성동구
    '11215': 148, // 광진구
    '11440': 129, // 마포구
    '11590': 125, // 동작구
    '11740': 116, // 강동구
    '11140': 116, // 중구
    '11110': 109, // 종로구
    '11560': 107, // 영등포구
    '11470': 95,  // 양천구
    '11230': 85,  // 동대문구
    '11410': 80,  // 서대문구
    '11500': 75,  // 강서구
    '11290': 72,  // 성북구
    '11380': 60,  // 은평구
    '11260': 52,  // 중랑구
    '11545': 20,  // 금천구
    '11620': 20,  // 관악구
    '11530': 20,  // 구로구
    '11350': 20,  // 노원구
    '11320': 20,  // 도봉구
    '11305': 20,  // 강북구
  };
  if (lawdCd in map) return map[lawdCd];
  if (lawdCd.startsWith('41')) return 15; // 경기도
  if (lawdCd.startsWith('28')) return 5;  // 인천
  return 50;
}
