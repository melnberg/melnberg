// 텍스트 정규화 — 공백/특수문자 압축 후 비교용 키.
function normText(s: string): string {
  return s.replace(/\s+/g, '').replace(/["'·.,!?~()「」『』]/g, '').trim();
}

// OpenAI gpt-5-mini 로 사주팔자 풍 운세 1건 생성.
// 매 호출마다 카테고리·12지·오행·방위·시진·톤을 랜덤 주입해서 다양성 폭발 유도.
async function generateFortuneWithAI(
  forbidden: string[],
  dayName: string,
  isWeekend: boolean,
  variantSeed: number,
): Promise<string | null> {
  if (!process.env.OPENAI_API_KEY) return null;

  // 매 호출마다 다른 변수 조합 — variantSeed 가 1씩 증가하면서 다른 카테고리로 회전
  const category = CATEGORIES[(variantSeed * 7) % CATEGORIES.length];
  const tone = pick(TONES);
  const element = pick(ELEMENTS);
  const zodiac = pick(ZODIACS);
  const direction = pick(DIRECTIONS);
  const color = pick(COLORS);
  const luckyNum = pick(NUMBERS);
  const hour = pick(HOURS);
  const season = pick(SEASONS_24);
  const hint = pick(HINTS);

  const recentForbidden = forbidden.slice(0, 40);
  const forbiddenBlock = recentForbidden.length > 0
    ? `\n\n[절대 금지]\n다음 문구들과 같거나 비슷한 표현·구조 절대 금지. 다른 어휘·다른 비유로 새로 써:\n${recentForbidden.map((t, i) => `${i + 1}. ${t}`).join('\n')}`
    : '';

  const SYSTEM = `너는 사주명리·주역·전통 점술에 정통한 한국 도사야. 오늘의 운세 한 문장을 신비롭고 주술적인 톤으로 뽑아.

[형식]
- 한 문장, 60~140자.
- **해요체** 종결만 사용 ("~예요", "~해요", "~돼요", "~따라요", "~좋아요", "~하세요"). 음슴체("~함", "~됨", "~할 것") 절대 금지.
- 출력은 운세 한 문장만. 따옴표·번호·해설·이모지 없음.
- 운세 톤: ${tone}

[필수 — 사주·주술 요소 3개 이상 자연스럽게 녹여 쓸 것]
- 운세 분야: ${category} (이 분야를 중심으로)
- 오행 키워드: ${element}
- 12지 키워드: ${zodiac} (지지의 기운으로 활용)
- 방위: ${direction}
- 오방색: ${color}
- 행운 숫자: ${luckyNum}
- 시진(시간대): ${hour}
- 24절기 또는 별·달·해·바람·구름 같은 천체 어휘 1개 (예: ${season}, 북두칠성, 달빛 등)
- 일상 행동 힌트 1개 (예: ${hint})

[금지]
- 인스타·카톡·SNS·이메일·메신저 같은 현대 단어 ❌
- "직원 추천", "메뉴 결정", "카페 가기" 같은 일상 잡사 ❌
- 너무 평범하거나 운세 같지 않은 문장 ❌
- ${isWeekend ? '주말이라 **직장·출근·회의·퇴근·미팅·야근·결재** 절대 사용 금지' : '평일'}

[좋은 예시]
- "토(土)의 기운이 황금색으로 흐르는 진시(오전 7~9시)예요. 동쪽 창가에 맑은 물 한 잔을 두면 재물문이 세 번 열려요."
- "용(辰)의 기운이 어깨에 도는 날이에요. 자색 한 점을 가까이 두면 귀인이 서북쪽에서 다가와요."
- "북두칠성의 한 별이 잠시 머무는 술시(저녁 7~9시)예요. 묵은 인연한테 짧게 안부 한 줄이면 적색 실이 다시 이어져요."${forbiddenBlock}`;

  try {
    const ctrl = new AbortController();
    const timer = setTimeout(() => ctrl.abort(), 5000);
    const res = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      signal: ctrl.signal,
      headers: {
        'Authorization': `Bearer ${process.env.OPENAI_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-5-mini',
        messages: [
          { role: 'system', content: SYSTEM },
          { role: 'user', content: `오늘은 ${dayName}요일${isWeekend ? ' (주말)' : ' (평일)'}이에요. ${category} 중심으로 위 요소들을 녹여 한 문장. forbidden 과 절대 겹치지 않게.` },
        ],
        max_completion_tokens: 200,
      }),
    });
    clearTimeout(timer);
    if (!res.ok) return null;
    const j = await res.json();
    const raw = j?.choices?.[0]?.message?.content;
    if (typeof raw !== 'string') return null;
    const text = raw.trim().replace(/^["「『]/, '').replace(/["」』]$/, '').trim();
    if (text.length < 25 || text.length > 300) return null;
    return text;
  } catch {
    return null;
  }
}

export async function POST() {
  const supabase = await createClient();
  const { data: { user } } = await supabase.auth.getUser();
  if (!user) return NextResponse.json({ error: '로그인이 필요해요' }, { status: 401 });

  // KST 기준 오늘 날짜 + 요일 (주말/평일 인지용)
  const kstNow = new Date(Date.now() + 9 * 3600_000);
  const today = kstNow.toISOString().slice(0, 10);
  const dayNames = ['일', '월', '화', '수', '목', '금', '토'];
  const dayName = dayNames[kstNow.getUTCDay()];
  const isWeekend = dayName === '토' || dayName === '일';

  // 이미 오늘치 뽑았으면 그것 반환
  const { data: existing } = await supabase
    .from('fortune_cookies')
    .select('id, fortune_text, drawn_date, created_at')
    .eq('user_id', user.id)
    .eq('drawn_date', today)
    .is('deleted_at', null)
    .maybeSingle();
  if (existing) {
    return NextResponse.json({ ok: true, already: true, fortune: existing });
  }

  // 중복 차단 — 오늘 전체 + 본인 과거 500개
  const [{ data: todayRows }, { data: mineRows }] = await Promise.all([
    supabase
      .from('fortune_cookies')
      .select('fortune_text')
      .eq('drawn_date', today)
      .is('deleted_at', null),
    supabase
      .from('fortune_cookies')
      .select('fortune_text')
      .eq('user_id', user.id)
      .is('deleted_at', null)
      .order('created_at', { ascending: false })
      .limit(500),
  ]);
  const forbidden: string[] = [
    ...((todayRows ?? []) as Array<{ fortune_text: string }>).map((r) => r.fortune_text),
    ...((mineRows ?? []) as Array<{ fortune_text: string }>).map((r) => r.fortune_text),
  ];
  const forbiddenSet = new Set(forbidden.map(normText));

  // AI 시도 — 최대 4회. 매 시도마다 다른 카테고리·요소 조합. 중복이면 재시도.
  let fortune: string | null = null;
  for (let attempt = 0; attempt < 4; attempt++) {
    const candidate = await generateFortuneWithAI(forbidden, dayName, isWeekend, attempt);
    if (!candidate) break;  // 키/네트워크 문제 — 더 시도해도 의미 없음
    if (!forbiddenSet.has(normText(candidate))) { fortune = candidate; break; }
  }

  // AI 실패·전부 중복 시 fallback — FORTUNES 에서 forbidden 제외하고 user×date 해시로 선택
  if (!fortune) {
    const pool = FORTUNES.filter((t) => !forbiddenSet.has(normText(t)));
    const source = pool.length > 0 ? pool : FORTUNES;
    let hash = 0;
    const seed = `${user.id}-${today}`;
    for (let i = 0; i < seed.length; i++) hash = (hash * 131 + seed.charCodeAt(i)) | 0;
    const idx = ((hash % source.length) + source.length) % source.length;
    fortune = source[idx];
  }

  const { data: row, error } = await supabase
    .from('fortune_cookies')
    .insert({ user_id: user.id, fortune_text: fortune, drawn_date: today })
    .select('id, fortune_text, drawn_date, created_at')
    .single();
  if (error || !row) {
    // 동시 insert 충돌 시 다시 select 로 fallback
    const { data: again } = await supabase
      .from('fortune_cookies')
      .select('id, fortune_text, drawn_date, created_at')
      .eq('user_id', user.id)
      .eq('drawn_date', today)
      .maybeSingle();
    if (again) return NextResponse.json({ ok: true, already: true, fortune: again });
    return NextResponse.json({ error: error?.message ?? '뽑기 실패' }, { status: 500 });
  }

  return NextResponse.json({ ok: true, already: false, fortune: row });
}
