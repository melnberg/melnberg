const groupArg = process.argv[2];
const ymdFrom = process.argv[3];
const ymdTo = process.argv[4] || ymdFrom;

if (!groupArg || !ymdFrom) {
  console.error('사용법: node scripts/molit-bulk.mjs <group> <YYYYMM_FROM> [<YYYYMM_TO>]');
  console.error('그룹: seoul, metro, five_cities, sejong, province_hot, all');
  process.exit(1);
}

const targets = groupArg === 'all'
  ? [
      ...GROUPS.seoul,
      ...GROUPS.metro,
      ...GROUPS.five_cities,
      ...GROUPS.sejong,
      ...GROUPS.province_hot,
    ]
  : groupArg === 'capital'
    ? [...GROUPS.seoul, ...GROUPS.metro]
    : GROUPS[groupArg];

if (!targets) {
  console.error(`알 수 없는 그룹: ${groupArg}`);
  console.error(`가능: ${Object.keys(GROUPS).join(', ')}, all`);
  process.exit(1);
}

console.log(`\n[일괄 적재] 그룹=${groupArg}, 시군구 ${targets.length}개, ${ymdFrom} ~ ${ymdTo}\n`);

const startedAt = Date.now();
let okCount = 0;
let failCount = 0;
const failures = [];

for (let i = 0; i < targets.length; i++) {
  const [code, label] = targets[i];
  const idx = `[${i + 1}/${targets.length}]`;
  process.stdout.write(`${idx} ${label} (${code})... `);

  const result = spawnSync('node', ['scripts/molit-ingest.mjs', code, ymdFrom, ymdTo], {
    stdio: 'inherit',
  });

  if (result.status === 0) {
    okCount++;
  } else {
    failCount++;
    failures.push(`${label} (${code})`);
  }
}

const elapsedMin = ((Date.now() - startedAt) / 60000).toFixed(1);
console.log(`\n[완료] 성공 ${okCount}/${targets.length}, 실패 ${failCount}, 소요 ${elapsedMin}분`);
if (failures.length > 0) {
  console.log(`실패 목록:\n  ${failures.join('\n  ')}`);
}
